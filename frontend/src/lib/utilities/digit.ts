export const cell = (value: string, index: number) => {
  const digit = digits[value as DigitKey];

  if(digit) {
    const symbol = digit[index] as RotationKey;
    const pair = rotation[symbol];
    if(pair) return pair;
  }

  return rotation[" "];
}